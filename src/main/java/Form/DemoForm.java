    private void jAddBtnActionPerformed(java.awt.event.ActionEvent evt) {
        ITRepository repo = new ITRepository();
        try{
            repo.addRequest(jTextName.getText(),jTextDate.getText(),jTextEmail.getText(),jTextType.getText(),jTextDetails.getText());
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Thêm request thất bại rồi!");
        }
    }
