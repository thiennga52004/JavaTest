    private void jFindBtnActionPerformed(java.awt.event.ActionEvent evt) {                                         
        ITRepository repo= new ITRepository();
        ITRequest request = new ITRequest();
        try{
           request = repo.findById(jTextId.getText());
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Tìm thất bại!");
        }
        String reqid = String.valueOf(request.getReqId());
        jTextId.setText(reqid);
        jTextName.setText(request.getReqName());
        jTextDate.setText(String.valueOf(request.getReqDate()));
        jTextEmail.setText(request.getReqEmail());
        jTextType.setText(request.getReqType());
        jTextDetails.setText(request.getReqDetails());
    }

